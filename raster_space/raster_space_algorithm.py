        prj = input_gdal.GetProjectionRef()
        dst_ds.SetGeoTransform(gt)
        dst_ds.SetProjection(prj)

        dstband = dst_ds.GetRasterBand(1)
        dstband.SetNoDataValue(nodata)
        dstband.WriteArray(npres.astype(np.int16), 0, 0)
        dstband.FlushCache()

        return

